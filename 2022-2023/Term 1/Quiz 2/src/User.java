public abstract class User
{
    // ================================================= Variables =================================================
    private String name;
    private String address;

    // ================================================= Getters =================================================
    public String getName()
    {
        return name;
    }
    public String getAddress()
    {
        return address;
    }

    // ================================================= Setters =================================================
    public void setName(String name)
    {
        this.name = name;
    }
    public void setAddress(String address)
    {
        this.address = address;
    }
}
